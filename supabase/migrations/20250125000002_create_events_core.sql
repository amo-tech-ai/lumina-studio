-- RLS POLICIES FOR EVENT SCHEDULES
-- ============================================

-- Public can view schedules for published events
create policy "anon can view published event schedules"
  on event_schedules for select
  to anon
  using (
    exists (
      select 1 from events
      where events.id = event_schedules.event_id
      and events.status = 'published'
      and events.is_public = true
    )
  );
-- Authenticated can view schedules for published events
create policy "authenticated can view published event schedules"
  on event_schedules for select
  to authenticated
  using (
    exists (
      select 1 from events
      where events.id = event_schedules.event_id
      and events.status = 'published'
      and events.is_public = true
    )
  );
-- Organizers can view schedules for their events
create policy "organizers can view event schedules"
  on event_schedules for select
  to authenticated
  using (
    exists (
      select 1 from events
      where events.id = event_schedules.event_id
      and events.organizer_id = auth.uid()
    )
  );
-- Organizers can insert schedules for their events
create policy "organizers can insert event schedules"
  on event_schedules for insert
  to authenticated
  with check (
    exists (
      select 1 from events
      where events.id = event_schedules.event_id
      and events.organizer_id = auth.uid()
    )
  );
-- Organizers can update schedules for their events
create policy "organizers can update event schedules"
  on event_schedules for update
  to authenticated
  using (
    exists (
      select 1 from events
      where events.id = event_schedules.event_id
      and events.organizer_id = auth.uid()
    )
